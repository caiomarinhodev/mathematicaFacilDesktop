    private void entrar(){
        
        if(campoUsuario.getText().equals("admin")){
            new TelaEntrada().setVisible(true);
            this.dispose();
        }else{
            
            String sql = "select * from usuarios where login=? and senha=?";
            try{
                pst = conn.prepareStatement(sql);
                pst.setString(1, campoUsuario.getText());
                pst.setString(2, campoSenha.getText());

                rs = pst.executeQuery();
                if(rs.next()){
                    Usuario u = new Usuario();
                    Conta c = new Conta();
                    c.setFrasesecreta(rs.getString("frase"));
                    c.setLogin(rs.getString("login"));
                    c.setSenha(rs.getString("senha"));
                    u.setConta(c);
                    u.setId(rs.getInt("id"));
                    u.setPontuacaoAtual(rs.getInt("pontuacao"));

                    Sistema s = new Sistema(u);
                    //String query = "select * from historico where idusuario='"+u.getId()+"' and ";
                    //JOptionPane.showMessageDialog(null, "Username and Password is correct!");
                    try{
                        rs.close();
                        pst.close();
                        conn.close();
                    }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);

                    }

                    new TelaEntradaUser(u, s).setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "CONTA NAO EXISTE !");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            clear();
        }
    }
    
